    public Formulario_Inserir_Gasto(Gasto gasto) {
        initComponents();
        KeyAdapter numOnly = new KeyAdapter(){
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if ( ((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE) && (c!=',')) {
                    e.consume();
                }
            }
        };
        jTF_Valor.addKeyListener(numOnly);
        jTF_Abatimento.addKeyListener(numOnly);
        jTF_Juros.addKeyListener(numOnly);
        
        jTF_Descricao.setText(gasto.getDescricao());
        jTF_Valor.setText(String.valueOf(gasto.getValor()).replace(".", ","));
        jCB_Dia.setSelectedItem(String.valueOf(gasto.getDia()));
        String[] mes_ano = gasto.getMes().split("/");
        String mes = mes_ano[0];
        String ano = mes_ano[1];
        jCB_Mes.setSelectedItem(mes);
        jCB_Ano.setSelectedItem(ano);
        if(gasto.isAbatimento()){
            jCB_Abatimento.setSelected(true);
            jTF_Abatimento.setText(String.valueOf(gasto.getAbatimento_val()).replace(".", ","));
        }
        if(gasto.isJuros()){
            jCB_Juros.setSelected(true);
            jTF_Juros.setText(String.valueOf(gasto.getJuros_val()).replace(".", ","));
        }
        setUpdate(true);
        setGasto(gasto);
    }
    
