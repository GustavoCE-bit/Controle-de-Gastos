    private void jTF_ValorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTF_ValorKeyTyped
        String val = jTF_Valor.getText();
        char c = evt.getKeyChar();
        int ponto = val.indexOf(",")+3;
        int cursor = jTF_Valor.getCaretPosition();
        if(c==',' && val.contains(",")){
            evt.consume();
        }
        if(val.contains(",") && val.length()==ponto && cursor>ponto-3){
            evt.consume();
        }
        if(c==',' && cursor<val.length()-2){
            evt.consume();
        }
    }//GEN-LAST:event_jTF_ValorKeyTyped

