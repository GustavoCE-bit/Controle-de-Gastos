    private void jB_ContinuarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jB_ContinuarMouseClicked
        if(jTF_Descricao.getText().isEmpty() || jTF_Valor.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null,"Deve colocar descrição e valor do gasto");
                }else{
                    if(jCB_Abatimento.isSelected() && jTF_Abatimento.getText().isEmpty()){
                        JOptionPane.showMessageDialog(null,"Deve colocar o valor do abatimento");
                    }else{
                        if(jCB_Juros.isSelected() && jTF_Juros.getText().isEmpty()){
                            JOptionPane.showMessageDialog(null,"Deve colocar o valor do juros");
                        }else{
                            try{
                                Gasto gasto = new Gasto();
                                Consultas conn = new Consultas();
                                if(isUpdate()){
                                    gasto.setId(getGasto().getId());
                                }else{
                                    gasto.setId(conn.getNewID("gastos"));
                                }
                                
                                gasto.setDescricao(jTF_Descricao.getText());
                                System.out.println(gasto.getDescricao());
                                
                                int diaInt = Integer.parseInt(jCB_Dia.getSelectedItem().toString());
                                String mesString = (jCB_Mes.getSelectedItem()+"/"+jCB_Ano.getSelectedItem());
                                gasto.setDiaID(conn.getDiaID(diaInt, mesString));
                                gasto.setDia(diaInt);
                                gasto.setMes(mesString);
                                System.out.println(gasto.getDia()+"/"+gasto.getMes());
                                
                                gasto.setValor(Double.parseDouble(jTF_Valor.getText().replace(',', '.')));
                                System.out.println(gasto.getValor());
                                if(jCB_Abatimento.isSelected()){
                                    gasto.setAbatimento(true);
                                    gasto.setAbatimento_val(Double.parseDouble(jTF_Abatimento.getText().replace(',', '.')));
                                }else{
                                    gasto.setAbatimento(false);
                                    gasto.setAbatimento_val(0);
                                }
                                System.out.println(gasto.isAbatimento());
                                System.out.println(gasto.getAbatimento_val());
                                if(jCB_Juros.isSelected()){
                                    gasto.setJuros(true);
                                    gasto.setJuros_val(Double.parseDouble(jTF_Juros.getText().replace(',', '.')));
                                }else{
                                    gasto.setJuros(false);
                                    gasto.setJuros_val(0);
                                }
                                System.out.println(gasto.isJuros());
                                System.out.println(gasto.getJuros_val());
                                gasto.setValor_total(gasto.getValor()-gasto.getAbatimento_val()+gasto.getJuros_val());
                                System.out.println(gasto.getValor_total());
                                   
                                if(isUpdate()){
                                    boolean sucesso = conn.updateGasto(gasto);
                                    if(sucesso){
                                        JOptionPane.showMessageDialog(null, "Gasto atualizado com sucesso");
                                    }else{
                                        JOptionPane.showMessageDialog(null, "Erro ao atualizar gasto");
                                    }
                                    dispose();
                                }else{
                                    boolean sucesso = conn.inserirGasto(gasto);
                                    if(sucesso){
                                        JOptionPane.showMessageDialog(null, "Gasto inserido com sucesso");
                                    }else{
                                        JOptionPane.showMessageDialog(null, "Erro ao inserir gasto");
                                    }
                                }     
                            }catch(SQLException ex){
                                System.out.println("Erro ao inserir gasto");
                                System.out.println(ex);
                            }
                        }
                    }
                }
    }//GEN-LAST:event_jB_ContinuarMouseClicked

