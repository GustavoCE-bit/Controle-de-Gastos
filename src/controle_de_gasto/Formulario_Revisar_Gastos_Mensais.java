    public void Preencher_Tabela(){
        try{
            Consultas conn = new Consultas();
            ArrayList<Gasto_Mensal> result =  conn.getAllGastosMensais();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int rows = result.size();
            for(int i=0;i<rows;i++){
                model.addRow(new Object[]{result.get(i).getMes(),result.get(i).getValor()});
            }
        }catch(SQLException ex){
            System.out.println("Erro ao retornar gasto");
            System.out.println(ex);
        }
    }
    
